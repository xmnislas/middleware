    public Alumno deserializar(String contenido) throws ClassNotFoundException{
        Alumno alumno = null;
        
        try{
            alumno = objectMapper.readValue(contenido, Alumno.class);
        }catch(Exception e){
            e.printStackTrace();
        }
        
        return alumno;
    }
    
    public void iniciarSockets(){
        try{
            socket = new Socket(host, puerto);
            entrada = new BufferedReader(new InputStreamReader(socket.getInputStream()));
            salida = new BufferedWriter(new OutputStreamWriter(socket.getOutputStream()));
        } catch (IOException ex) {
            Logger.getLogger(Aplicacion2GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

