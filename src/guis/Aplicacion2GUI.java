    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        Alumno alumno = new Alumno(txtNombreAlumno.getText(), Integer.parseInt(txtEdadAlumno.getText()));
        
        String alumnoJson = null;
        
        try{
            alumnoJson = objectMapper.writeValueAsString(alumno);
        }catch(Exception e){
            System.out.println("Error: "+e.getMessage());
        }
                
        try{
            salida.write("Json");
            salida.newLine();
            salida.write(alumnoJson);
            salida.newLine();
            salida.write(this.cbxDestino.getSelectedItem().toString());
            salida.newLine();
            salida.flush();
        }catch(Exception e){
            System.out.println("Error: "+e.getMessage());
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

