    @Override
    public void run() {
        this.setVisible(true);
        
        while(true){
            try{
                if(entrada.ready()){
                    String contenido = entrada.readLine();
                    String entidadRecibida = entrada.readLine();
                    Alumno alumno = deserializar(contenido);
                    txtNombreUsuario.setText(alumno.getNombre());
                    txtEdadUsuario.setText(Integer.toString(alumno.getEdad()));
                    lblEntRecibida.setText(entidadRecibida);
                }
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }
